# Helper: normalize slugs
def normalize_slug(s: str) -> str:
    s = s.lower()
    s = re.sub(r"[^a-z0-9]+", "_", s)
    s = re.sub(r"_{2,}", "_", s).strip("_")
    return s

async def process_single_parsed_query(
    structured_query: StructuredQuery,
    faiss_store: FAISS,                  # <-- Pass the FAISS store directly
    embeddings: HuggingFaceEmbeddings,
    #retriever: VectorStoreRetriever,
    answer_generator: AnswerGenerator,
    *,
    top_k_total: int = 15,
    keyword_weight: float = 3.0
) -> str:
    """
    1. Weighted retrieval across sub-queries with a shared top-K cap.
    2. **Reranking removed**: use pooled_docs directly.
    3. Generate answer from the pooled clauses.
    """
    loop = asyncio.get_running_loop()

    def _clean(text: str) -> str:
        return text.replace("_", " ").strip()

    sub_queries = structured_query.sub_query or [structured_query.raw_query]
    seen, pooled_docs = set(), []

    # 1️⃣ RETRIEVAL -----------------------------------------
    t0 = time.perf_counter()
    for sq in sub_queries:
        if len(pooled_docs) >= top_k_total:
            break
        docs = await loop.run_in_executor(
            None,
            weighted_similarity_search,
            faiss_store,
            embeddings,
            _clean(sq),
            structured_query.key_word,
            top_k_total,
            keyword_weight
        )
        for d in docs:
            sig = d.page_content[:120]
            if sig not in seen:
                seen.add(sig)
                pooled_docs.append(d)
            if len(pooled_docs) >= top_k_total:
                break
    print(f"> Retrieval pooled {len(pooled_docs)} unique chunks in {time.perf_counter() - t0:.2f} s.")

    # 2️⃣ SKIP RE-RANK: use first top_k_total pooled docs --------------
    final_docs = pooled_docs[:top_k_total]

    # 3️⃣ ANSWER GENERATION -------------------------------
    t2 = time.perf_counter()
    answer_obj = await answer_generator.generate_answer(
        structured_query=structured_query,
        final_documents=final_docs
    )
    print(f"> Answer generation took {time.perf_counter() - t2:.2f} s.")
    return answer_obj.answer

async def run_full_pipeline(pdf_url: str, questions: list[str]) -> dict:
    """
    Main orchestrator: process PDF, build Retriever + FAISS store, then answer questions.
    """
    total_start_time = time.perf_counter()
    print("--- Initializing Pipeline Components ---")
    llm = ChatOpenAI(temperature=0.0, model='gpt-4o')
    embeddings = HuggingFaceEmbeddings(model_name="sentence-transformers/all-MiniLM-L6-v2")
    pdf_processor = PDFProcessor()
    vector_store_manager = VectorStoreManager(embeddings=embeddings)
    query_parser = QueryParser(llm=llm)
    answer_generator = AnswerGenerator(llm=llm)
    
    # --- Step 1: Retriever & FAISS store preparation with caching ---
    url_hash = hashlib.md5(pdf_url.encode()).hexdigest()
    index_path = f"faiss_index_{url_hash}"

    if os.path.exists(index_path):
        #retriever = vector_store_manager.load_retriever(index_path, k=10)
        faiss_store = FAISS.load_local(
            index_path,
            vector_store_manager.embeddings,
            allow_dangerous_deserialization=True
        )
    else:
        print(f"No cache for this URL. Building new index at {index_path}...")
        document_chunks = pdf_processor.load_and_chunk_from_url(pdf_url)
        if not document_chunks:
            return {"error": "Failed to process the PDF document."}
        vector_store_manager.create_and_save_store(document_chunks, index_path)
        #retriever = vector_store_manager.load_retriever(index_path, k=10)
        # Attach underlying FAISS and embeddings to retriever for weighted search
        faiss_store = FAISS.load_local(
            index_path,
            vector_store_manager.embeddings,
            allow_dangerous_deserialization=True
    )

    # We still create a retriever, but we no longer try to modify it
    retriever = faiss_store.as_retriever(search_kwargs={"k": 10})
    # these lines were causing the error
    # retriever.vectorstore = faiss_store
    # retriever.embeddings = embeddings

    # --- Step 2: Parse queries in parallel ---
    print(f"\n--- Answering all {len(questions)} questions in parallel ---")
    parsing_tasks = [query_parser.parse_query(q) for q in questions]
    all_structured_queries = await asyncio.gather(*parsing_tasks)

    # --- Step 3: Answer all questions in parallel ---
    answer_tasks = [
        process_single_parsed_query(
            structured_query=sq,
            faiss_store=faiss_store,
            embeddings=embeddings,
            answer_generator=answer_generator
        ) for sq in all_structured_queries
    ]
    all_answers = await asyncio.gather(*answer_tasks)
    
    total_end_time = time.perf_counter()
    print(f"\n--- Total Pipeline Time: {total_end_time - total_start_time:.2f} seconds ---")
    return {"answers": all_answers}

